# import math module first
import math

# take input from user
num = int(input("Enter a number: "))

square_root = math.sqrt(num)  # calculate square root using math module
print(f"The square root of {num} is: {int(square_root)}")  # print the result

log = math.log(num)  # calculate natural logarithm using math module
print(f"The natural logarithm of {num} is: {log}")  # print the result

sine = math.sin(num)  # calculate sine using math module
print(f"The sine of {num} is: {sine}")  # print the result
